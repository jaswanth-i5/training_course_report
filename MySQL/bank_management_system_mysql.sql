DROP DATABASE IF EXISTS bank_management_system;
CREATE DATABASE bank_management_system CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE bank_management_system;


CREATE TABLE branches (
  branch_id INT AUTO_INCREMENT PRIMARY KEY,
  branch_code INT NOT NULL UNIQUE,
  branch_name VARCHAR(100) NOT NULL,
  address VARCHAR(200) NOT NULL,
  city VARCHAR(80) NOT NULL,
  state VARCHAR(40) NOT NULL,
  country VARCHAR(40) NOT NULL,
  ifsc_code VARCHAR(16) NOT NULL UNIQUE,
  phone VARCHAR(15) NOT NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE customers (
  customer_id INT AUTO_INCREMENT PRIMARY KEY,
  first_name VARCHAR(60) NOT NULL,
  last_name VARCHAR(60) NOT NULL,
  email VARCHAR(120) NOT NULL UNIQUE,
  phone VARCHAR(15) NOT NULL UNIQUE,
  address VARCHAR(200) NOT NULL,
  city VARCHAR(80) NOT NULL,
  state VARCHAR(40) NOT NULL,
  postal_code VARCHAR(12) NOT NULL,
  dob DATE NOT NULL,
  created_at DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE employees (
  employee_id INT AUTO_INCREMENT PRIMARY KEY,
  branch_id INT NOT NULL,
  first_name VARCHAR(60) NOT NULL,
  last_name VARCHAR(60) NOT NULL,
  email VARCHAR(120) NOT NULL UNIQUE,
  phone VARCHAR(15) NOT NULL UNIQUE,
  role ENUM('teller','manager','auditor','service_rep','loan_officer') NOT NULL,
  hired_at DATE NOT NULL,
  salary DECIMAL(10,2) NOT NULL CHECK (salary >= 15000),
  manager_id INT NULL,
  CONSTRAINT fk_emp_branch FOREIGN KEY (branch_id) REFERENCES branches(branch_id),
  CONSTRAINT fk_emp_manager FOREIGN KEY (manager_id) REFERENCES employees(employee_id)
    ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE accounts (
  account_id BIGINT AUTO_INCREMENT PRIMARY KEY,
  customer_id INT NOT NULL,
  branch_id INT NOT NULL,
  account_number BIGINT NOT NULL UNIQUE,
  account_type ENUM('savings','current','salary','fixed_deposit') NOT NULL,
  opened_at DATE NOT NULL,
  status ENUM('active','suspended','closed') NOT NULL DEFAULT 'active',
  balance DECIMAL(15,2) NOT NULL CHECK (balance >= 0),
  interest_rate DECIMAL(5,2) NULL,
  CONSTRAINT fk_acc_customer FOREIGN KEY (customer_id) REFERENCES customers(customer_id),
  CONSTRAINT fk_acc_branch FOREIGN KEY (branch_id) REFERENCES branches(branch_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE cards (
  card_id BIGINT AUTO_INCREMENT PRIMARY KEY,
  account_id BIGINT NOT NULL,
  card_number BIGINT NOT NULL UNIQUE,
  card_type ENUM('debit','credit') NOT NULL,
  issued_at DATE NOT NULL,
  expiry DATE NOT NULL,
  status ENUM('active','blocked','expired') NOT NULL DEFAULT 'active',
  CONSTRAINT fk_card_account FOREIGN KEY (account_id) REFERENCES accounts(account_id)
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE transactions (
  transaction_id BIGINT AUTO_INCREMENT PRIMARY KEY,
  account_id BIGINT NOT NULL,
  txn_time DATETIME NOT NULL,
  txn_type ENUM('credit','debit') NOT NULL,
  amount DECIMAL(15,2) NOT NULL CHECK (amount > 0),
  description VARCHAR(200) NULL,
  balance_after DECIMAL(15,2) NULL,
  CONSTRAINT fk_txn_account FOREIGN KEY (account_id) REFERENCES accounts(account_id)
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE loans (
  loan_id BIGINT AUTO_INCREMENT PRIMARY KEY,
  customer_id INT NOT NULL,
  branch_id INT NOT NULL,
  account_id BIGINT NULL,
  loan_type ENUM('home','auto','personal','education','business','gold') NOT NULL,
  principal DECIMAL(15,2) NOT NULL CHECK (principal > 0),
  interest_rate DECIMAL(5,2) NOT NULL CHECK (interest_rate > 0),
  term_months INT NOT NULL CHECK (term_months >= 6 AND term_months <= 360),
  start_date DATE NOT NULL,
  status ENUM('approved','active','closed','defaulted') NOT NULL DEFAULT 'active',
  CONSTRAINT fk_loan_customer FOREIGN KEY (customer_id) REFERENCES customers(customer_id),
  CONSTRAINT fk_loan_branch FOREIGN KEY (branch_id) REFERENCES branches(branch_id),
  CONSTRAINT fk_loan_account FOREIGN KEY (account_id) REFERENCES accounts(account_id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE loan_payments (
  payment_id BIGINT AUTO_INCREMENT PRIMARY KEY,
  loan_id BIGINT NOT NULL,
  due_date DATE NOT NULL,
  payment_date DATE NULL,
  amount DECIMAL(15,2) NOT NULL CHECK (amount > 0),
  principal_component DECIMAL(15,2) NOT NULL,
  interest_component DECIMAL(15,2) NOT NULL,
  status ENUM('due','paid','late') NOT NULL DEFAULT 'due',
  CONSTRAINT fk_payment_loan FOREIGN KEY (loan_id) REFERENCES loans(loan_id)
    ON DELETE CASCADE
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

